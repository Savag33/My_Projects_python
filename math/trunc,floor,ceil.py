# ети функуии находяться в модуру math
# в  trunc можно передеать только одно число
# функция - отсекает дробную часть (оставляет целую)

import math

print(math.trunc(32.999999)) # аналог int
print()
print(math.floor(32.9))  # округляет вниз (идет к ближайшему целому цисло которое не привосходит наше)
print(math.floor(-3.2))
print(math.floor(5))
print()
print(math.ceil(4.5)) # округляет аналогично как и floor только вверх
print(math.ceil(4.1))
print(math.ceil(-3.9))


